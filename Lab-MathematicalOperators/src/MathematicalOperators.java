public class MathematicalOperators {
    public static void main(String[] args){
        //define variables
        int x = 8;
        int y = 5;
        int z;

        //simple addition
        z = x + y;
        System.out.println(z);

        //simple subtraction
        z = x - y;
        System.out.println(z);

        //simple multiplication
        z = x * y;
        System.out.println(z);

        //simple division
        z = x / y;
        System.out.println(z);

        //simple modulus
        z = x % y;
        System.out.println(z);

        //float vs. int
        float f = 4.6f;
        int i = 34;
        double result = f + i;

        System.out.println(result);
        }
    }

